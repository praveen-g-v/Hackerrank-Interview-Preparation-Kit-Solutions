        SinglyLinkedListNode currHead1=head1,currHead2=head2;
        while(currHead1!=currHead2){
            if(currHead1.next == null){
                currHead1 = head2;
            }
            else{
                currHead1 = currHead1.next;
            }
            if(currHead2.next == null){
                currHead2 = head1;
            }
            else{
                currHead2= currHead2.next;
            }
            
        }
     return currHead2.data;
